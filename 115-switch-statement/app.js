//switch statement to log the days
let Saturday = 1;
switch (Saturday) {
    case 0:
        console.log(`It is Saturday !`);
        break;
    case 1:
        console.log(`It is Sunday !`);
        break;
    case 2:
        console.log(`It is Monday !`);
        break;
    case 3:
        console.log(`It is Tuesday !`);
        break;
    case 4:
        console.log(`It is Wednesday !`);
        break;
    case 5:
        console.log(`It is Thursday !`);
        break;
    case 6:
        console.log(`It is Friday !`);
        break;
    default:
        console.log(`No any Day !`);
        break;
}
export {};
